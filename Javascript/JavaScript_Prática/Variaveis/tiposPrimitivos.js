let condicao = true;
let twoCondicao = false;

console.log(condicao);
console.log(twoCondicao);
/*Undefined, não recebe nenhum valor*/
let semValorAtribuido;

console.log(semValorAtribuido);

/*Null, exibe um valor vazio, ou alterar o valor para null*/
let valor = null;

console.log(valor);
